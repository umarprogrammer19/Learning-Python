# Store Data In Key Value Pair
user = {
    "name": "Umar Farooq",
    "age": None,
    "isAlive": True,
    "subjects": ["Javascript", "Typescript", "Python"],
}

# For Printing Simple One Value
print(user["name"])
print(user["age"])
print(user["isAlive"])
print(user["subjects"])

# For Changing Values 
user["name"] = "Ayesha"
user["age"] = 20
print(user["name"])
print(user["age"])

